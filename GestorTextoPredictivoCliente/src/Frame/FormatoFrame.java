    /**
     * Listener para modificar la muestra al seleccionar una nueva fuente
     */
    private static class ListSelectionListenerFuente implements ListSelectionListener {

        private final FormatoFrame f;

        /**
         * Contructor parametrizado del listener
         *
         * @param f Clase frame FormatoFrame
         */
        public ListSelectionListenerFuente(FormatoFrame f) {
            this.f = f;
        }

        /**
         * Modifica la muestra al seleccionar una nueva fuente
         */
        @Override
        public void valueChanged(ListSelectionEvent e) {
            boolean adjust = e.getValueIsAdjusting();
            if (!adjust) {
                JList list = (JList) e.getSource();
                int selections[] = list.getSelectedIndices();
                Object selectionValues[] = list.getSelectedValues();
                for (int i = 0, n = selections.length; i < n; i++) {
                    f.actualizaFuente(selectionValues[i].toString());
                }
            }
        }
    }

