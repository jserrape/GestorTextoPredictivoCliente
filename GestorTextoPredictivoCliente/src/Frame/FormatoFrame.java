    /**
     * Establece una nueva fuente al texto
     *
     * @param fuente Nueva fuente a establecer
     */
    private void actualizaFuente(String fuente) {
        this.jTextField1.setText(fuente);
        this.font = new Font(fuente, this.font.getStyle(), this.font.getSize());
        this.jTextArea1.setFont(font);
    }

    /**
     * Establece un nuevo estilo al texto
     *
     * @param est Nuevo estilo a esablecer
     */
    private void actualizaEstilo(String est) {
        int estilo;
        if ("Normal".equals(est)) {
            estilo = Font.PLAIN;
        } else {
            if ("Cursiva".equals(est)) {
                estilo = Font.ITALIC;
            } else {
                estilo = Font.BOLD;
            }
        }
        this.jTextField2.setText(est);
        this.font = new Font(this.font.getFontName(), estilo, this.font.getSize());
        this.jTextArea1.setFont(font);
    }

    /**
     * Establece un nuevo tamaño al texto
     *
     * @param tam Nuevo tamaño
     */
    private void actualizaTam(String tam) {
        this.jTextField4.setText(tam);
        this.font = new Font(this.font.getFontName(), this.font.getStyle(), Integer.parseInt(tam));
        this.jTextArea1.setFont(font);
    }

